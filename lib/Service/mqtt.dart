import 'dart:async';
import 'package:mqtt_client/mqtt_client.dart';
import 'package:mqtt_client/mqtt_server_client.dart';
import 'dart:math';

class Mqtt {
  final String broker = '192.168.243.95'; // Remplace par ton broker
  final String clientIdentifier = 'flutter_client_${Random().nextInt(100000)}';
  MqttServerClient? client;
  Timer? _reconnectTimer;

  Future<void> connect() async {
    client = MqttServerClient(broker, clientIdentifier);
    client!.logging(on: true);
    client!.keepAlivePeriod = 20;
    client!.onDisconnected = onDisconnected;
    client!.onConnected = onConnected;
    client!.onSubscribed = onSubscribed;

    final connMessage = MqttConnectMessage()
        .withClientIdentifier(clientIdentifier)
        .withWillQos(MqttQos.exactlyOnce);
    client!.connectionMessage = connMessage;

    try {
      await client!.connect();
      if (client!.connectionStatus!.state == MqttConnectionState.connected) {
        print('‚úÖ MQTT connect√©');
        _reconnectTimer?.cancel(); // Annule la reconnexion si c'est OK
      } else {
        print('‚ùå Connexion √©chou√©e, √©tat: ${client!.connectionStatus!.state}');
        client!.disconnect();
        _startReconnect(); // Tente une reconnexion
      }
    } catch (e) {
      print('‚ö†Ô∏è Exception MQTT: $e');
      client!.disconnect();
      _startReconnect(); // Tente une reconnexion
    }
  }

  void _startReconnect() {
    if (_reconnectTimer == null || !_reconnectTimer!.isActive) {
      print('üîÑ Tentative de reconnexion dans 5 secondes...');
      _reconnectTimer = Timer(Duration(seconds: 5), connect);
    }
  }

  void onConnected() {
    print('‚úÖ MQTT connect√©');
  }

  void onDisconnected() {
    print('‚ùå MQTT d√©connect√©, tentative de reconnexion...');
    _startReconnect(); // Relance la connexion automatiquement
  }

  void onSubscribed(String topic) {
    print('üìå Abonn√© √†: $topic');
  }

  void subscribeToTopic(String topic) {
    client!.subscribe(topic, MqttQos.exactlyOnce);
  }

  void publishMessage(String topic, String message) {
    final builder = MqttClientPayloadBuilder();
    builder.addString(message);
    client!.publishMessage(topic, MqttQos.exactlyOnce, builder.payload!);
  }
}
